def generate_override_theme(
    sub_name: str,
    import_path: str,
    primary: str,
    accent: str,
) -> str:
    """Return a minimal override _pdf-theme.css string."""
    return _OVERRIDE_TEMPLATE.substitute(
        sub_name=sub_name,
        import_path=import_path,
        primary=primary,
        accent=accent,
        stripe_bg=_tint(accent, 0.08),
        code_bg=_tint(accent, 0.06),
        pre_bg=_tint(accent, 0.08),
    )


