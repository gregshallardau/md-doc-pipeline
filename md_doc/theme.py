def generate_meta_yml(org_name: str, cover_page: bool) -> str:
    cover_str = "true" if cover_page else "false"
    return f"""author: {org_name}
cover_page: {cover_str}
outputs: [pdf]
"""


# ---------------------------------------------------------------------------
# Parent theme discovery
# ---------------------------------------------------------------------------

def find_parent_theme(from_dir: Path) -> Path | None:
    """
    Walk up from *from_dir*'s parent looking for a _pdf-theme.css.
    Returns the first one found (nearest ancestor), or None.
    """
    current = from_dir.resolve().parent
    while True:
        candidate = current / "_pdf-theme.css"
        if candidate.exists():
            return candidate
        parent = current.parent
        if parent == current:
            return None
        current = parent


def relative_import_path(from_dir: Path, theme_path: Path) -> str:
    """Return the CSS @import path from *from_dir* to *theme_path*."""
    rel = os.path.relpath(theme_path, from_dir)
    return rel.replace(os.sep, "/")
