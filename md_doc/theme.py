# ---------------------------------------------------------------------------
# Color helpers
# ---------------------------------------------------------------------------

def _tint(hex_color: str, factor: float = 0.08) -> str:
    """
    Blend *hex_color* toward white.

    ``factor`` is the proportion of the original color (0 = white, 1 = original).
    factor=0.08 produces a very light background tint suitable for table stripes
    and code block backgrounds.
    """
    h = hex_color.lstrip("#")
    r = int(h[0:2], 16)
    g = int(h[2:4], 16)
    b = int(h[4:6], 16)
    r = round(r * factor + 255 * (1 - factor))
    g = round(g * factor + 255 * (1 - factor))
    b = round(b * factor + 255 * (1 - factor))
    return f"#{r:02x}{g:02x}{b:02x}"


def validate_hex_color(value: str) -> str:
    """Return the colour normalised to '#rrggbb', or raise ValueError."""
    v = value.strip()
    if not v.startswith("#"):
        v = "#" + v
    v = v.lower()
    if len(v) == 4:
        # Expand shorthand #abc → #aabbcc
        v = "#" + v[1] * 2 + v[2] * 2 + v[3] * 2
    if len(v) != 7 or not all(c in "0123456789abcdef" for c in v[1:]):
        raise ValueError(f"'{value}' is not a valid hex colour (expected #rrggbb)")
    return v


