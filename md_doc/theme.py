def generate_base_theme(
    org_name: str,
    primary: str,
    accent: str,
    body_text: str,
    muted: str,
    body_font: str,
    mono_font: str,
    page_size: str,
) -> str:
    """Return a fully populated _pdf-theme.css string."""
    return _BASE_TEMPLATE.substitute(
        org_name=org_name,
        primary=primary,
        accent=accent,
        body_text=body_text,
        muted=muted,
        body_font=body_font,
        mono_font=mono_font,
        page_size=page_size,
        stripe_bg=_tint(accent, 0.08),
        code_bg=_tint(accent, 0.06),
        pre_bg=_tint(accent, 0.08),
    )


